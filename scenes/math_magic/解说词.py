# 保存文件名：generate_voice.py
# 功能：把 EP06 信息熵解说稿生成语音 mp3

import asyncio
import edge_tts

# 选择语音（微软支持很多，这里用中文普通话女声）
VOICE = "zh-CN-XiaoxiaoNeural"
OUTPUT_FILE = "EP06_voice.mp3"

# 旁白稿（带时间轴，可直接用）
SCRIPT = """
开场
你有没有想过，为什么 ZIP 文件能变小？
什么样的密码才算安全？
ChatGPT 又是怎么预测下一个词的？
这些看似不相关的问题，背后竟然有同一个答案：信息熵。

什么是信息量？
我们先来理解「信息量」。
简单来说，信息量就是消除不确定性的程度。
越出乎意料的事，信息量就越大；
反之，必然发生的事，信息量就是零。

比如抛硬币，结果正反各一半的概率。
这时候的信息量就是 1 个比特。
而如果一枚硬币总是正面朝上，那么结果是必然的，信息量就是 0。

香农熵：平均信息量
信息论之父香农发现，如果我们把一条消息里所有可能的情况平均考虑，
就得到了「熵」，也就是平均信息量。
它衡量了一段信息里的不确定性有多大。

熵越大，表示结果越难预测，越混乱。
而当概率分布均匀时，熵达到最大值。

熵与文件压缩
熵和压缩有密切关系。
比如「哈哈哈哈」这种重复的文本，电脑可以很轻松地缩短成「哈乘以10」。
而如果是一串完全随机的字符，就几乎无法压缩。

香农定理告诉我们：无损压缩的极限，就是信息熵。
这也是 ZIP、JPEG、MP3 这些格式背后的数学原理。

密码强度
密码的强度，其实也和熵有关。
像「123456」这种数字密码，很快就能被猜出。
而如果是由几个随机单词组成的长密码，破解的难度就大得多。

计算公式是：
密码熵 = 长度 × log2(字符集大小)。

这也给我们一个实用建议：
增加密码长度，比增加复杂度更有效。

AI 的核心：交叉熵
在人工智能里，交叉熵用来衡量模型预测和真实答案之间的差距。

比如输入「The weather is」，
模型可能预测「nice」的概率是 35%、「good」是 25%、「bad」是 15%。
如果真实答案是「good」，那么差距就是交叉熵衡量的部分。

训练 AI 的目标，就是不断减少这种差距。
换句话说，训练 AI = 最小化交叉熵。

总结
总结一下，信息熵不仅是一个数学公式，
它还是理解压缩、加密和人工智能的统一框架。

它让我们看到，世界的混乱中，其实隐藏着一种精确的秩序。
"""

async def main():
    communicate = edge_tts.Communicate(SCRIPT, VOICE)
    await communicate.save(OUTPUT_FILE)
    print(f"✅ 语音已生成：{OUTPUT_FILE}")

if __name__ == "__main__":
    asyncio.run(main())
